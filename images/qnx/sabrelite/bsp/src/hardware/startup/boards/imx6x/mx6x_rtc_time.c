#include "arm/mx6x.h"

unsigned long rtc_time(void)
{
	/*
	 * Tell Neutrino what kind of chip for 'rtc' utility
	 */
	hwi_add_rtc("mx6srtc", MX6X_SNVS_BASE, 0, MX6X_SNVS_SIZE, 1, -1);
	return 0;
}



