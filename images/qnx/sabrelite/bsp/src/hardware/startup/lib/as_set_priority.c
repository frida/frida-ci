#include "startup.h"

void
as_set_priority(unsigned off, unsigned priority) {
	struct asinfo_entry	*as = as_off2info(off);

	for( ;; ) {
		as->priority = priority;
		if(!(as->attr & AS_ATTR_CONTINUED)) break;
		++as;
	}
}



